package com.eyuanku.web.framework.util;

public class FilterConfig {
	private String ossEnv;
	private String filterServer;
	
	public String getOssEnv() {
		return ossEnv;
	}
	public void setOssEnv(String ossEnv) {
		this.ossEnv = ossEnv;
	}
	public String getFilterServer() {
		return filterServer;
	}
	public void setFilterServer(String filterServer) {
		this.filterServer = filterServer;
	}
	
	
}
